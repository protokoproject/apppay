    $code = $hasil['max_code'];

    if ($code) {
        $urutan = (int)$code + 1; // Increment the highest ID
    } else {
        $urutan = 1; // If the table is empty, start with 1
    }

    $idguru = $urutan;
    $query = mysqli_query($koneksi, "INSERT INTO t_guru(id_guru, sp_mapel) VALUES ('$idguru', '$spmapel')");
